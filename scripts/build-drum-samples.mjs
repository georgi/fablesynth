#!/usr/bin/env node
// Offline converter: committed mono PCM16 TR-808 WAVs -> the identical compact
// one-shot bank for the web AudioWorklet and the JUCE/headless C++ engine.

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = join(dirname(fileURLToPath(import.meta.url)), '..');
const SOURCES = [
  ['808SD', 'assets/drum-samples/808/808SD_SD5050.WAV'],
  ['808CP', 'assets/drum-samples/808/808CP_CP.WAV'],
  ['808CH', 'assets/drum-samples/808/808CH_CH.WAV'],
  ['808OH', 'assets/drum-samples/808/808OH_OH50.WAV'],
  ['808CY', 'assets/drum-samples/808/808CY_CY5050.WAV'],
];

function parseMonoPcm16(buf) {
  const u8 = new Uint8Array(buf.buffer, buf.byteOffset, buf.byteLength);
  const dv = new DataView(u8.buffer, u8.byteOffset, u8.byteLength);
  const tag = (at) => String.fromCharCode(u8[at], u8[at + 1], u8[at + 2], u8[at + 3]);
  if (tag(0) !== 'RIFF' || tag(8) !== 'WAVE') throw new Error('not RIFF/WAVE');
  let rate = 0, channels = 0, bits = 0, format = 0, dataAt = -1, dataBytes = 0;
  for (let at = 12; at + 8 <= u8.length;) {
    const size = dv.getUint32(at + 4, true);
    const body = at + 8;
    if (tag(at) === 'fmt ') {
      format = dv.getUint16(body, true);
      channels = dv.getUint16(body + 2, true);
      rate = dv.getUint32(body + 4, true);
      bits = dv.getUint16(body + 14, true);
    } else if (tag(at) === 'data') {
      dataAt = body;
      dataBytes = size;
    }
    at = body + size + (size & 1);
  }
  if (format !== 1 || channels !== 1 || bits !== 16 || dataAt < 0)
    throw new Error('expected mono PCM16 WAV');
  return { rate, pcm: Buffer.from(u8.subarray(dataAt, dataAt + dataBytes)) };
}

const samples = SOURCES.map(([name, file]) => ({ name, ...parseMonoPcm16(readFileSync(join(REPO, file))) }));

const ts = `// DO NOT EDIT — generated by scripts/build-drum-samples.mjs\n` +
`${SOURCES.map(([, file], i) => `import sample${i}Url from '../../../${file}?url';`).join('\n')}\n\n` +
`export interface DrumOneShot { name: string; sampleRate: number; data: Float32Array }\n\n` +
`const RAW = [\n${samples.map((s, i) => `  { name: '${s.name}', url: sample${i}Url },`).join('\n')}\n] as const;\n\n` +
`function decodeWavPcm16(buf: ArrayBuffer): { sampleRate: number; data: Float32Array } {\n` +
`  const u8 = new Uint8Array(buf); const dv = new DataView(buf);\n` +
`  const tag = (at: number) => String.fromCharCode(u8[at], u8[at + 1], u8[at + 2], u8[at + 3]);\n` +
`  let sampleRate = 0, dataAt = -1, dataBytes = 0;\n` +
`  for (let at = 12; at + 8 <= u8.length;) {\n` +
`    const size = dv.getUint32(at + 4, true); const body = at + 8;\n` +
`    if (tag(at) === 'fmt ') sampleRate = dv.getUint32(body + 4, true);\n` +
`    if (tag(at) === 'data') { dataAt = body; dataBytes = size; }\n` +
`    at = body + size + (size & 1);\n  }\n` +
`  if (!sampleRate || dataAt < 0) throw new Error('invalid drum sample WAV');\n` +
`  const data = new Float32Array(dataBytes / 2);\n` +
`  for (let i = 0; i < data.length; i++) data[i] = dv.getInt16(dataAt + i * 2, true) / 32768;\n` +
`  return { sampleRate, data };\n}\n\n` +
`export async function loadDrumOneShots(): Promise<DrumOneShot[]> {\n` +
`  return Promise.all(RAW.map(async (s) => ({ name: s.name, ...decodeWavPcm16(await (await fetch(s.url)).arrayBuffer()) })));\n}\n`;

const h = `// DO NOT EDIT — generated by scripts/build-drum-samples.mjs\n#pragma once\n\n#include <cstdint>\n#include <vector>\n\nnamespace fable {\nstruct DrumOneShot { const char* name; int sampleRate; const int16_t* data; int length; };\nconst std::vector<DrumOneShot>& drumOneShots();\n} // namespace fable\n`;

const arrays = samples.map((s, index) => {
  const values = new Int16Array(s.pcm.buffer, s.pcm.byteOffset, s.pcm.byteLength / 2);
  const rows = [];
  for (let at = 0; at < values.length; at += 20) rows.push('    ' + Array.from(values.subarray(at, at + 20)).join(', '));
  return `static const int16_t kSample${index}[] = {\n${rows.join(',\n')}\n};`;
}).join('\n\n');
const cpp = `// DO NOT EDIT — generated by scripts/build-drum-samples.mjs\n#include "OneShotSamples.gen.h"\n\nnamespace fable {\nnamespace {\n${arrays}\n} // namespace\n\nconst std::vector<DrumOneShot>& drumOneShots() {\n    static const std::vector<DrumOneShot> bank = {\n${samples.map((s, i) => `        { "${s.name}", ${s.rate}, kSample${i}, ${s.pcm.byteLength / 2} },`).join('\n')}\n    };\n    return bank;\n}\n} // namespace fable\n`;

writeFileSync(join(REPO, 'src/drum/engine/oneshots.gen.ts'), ts);
writeFileSync(join(REPO, 'juce/source/drum/dsp/OneShotSamples.gen.h'), h);
writeFileSync(join(REPO, 'juce/source/drum/dsp/OneShotSamples.gen.cpp'), cpp);
console.log(`Generated ${samples.length} one-shots: ${samples.map((s) => s.name).join(', ')}`);
